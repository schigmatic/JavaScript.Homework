const lang = "ru";
switch (lang) {
  case "ru":
    console.log("Добрый день!");
    break;
  case "en":
    console.log("Good afternoon!");
    break;
  case "de":
    console.log("Guten Tag!");
    break;
  case "fr":
    console.log("Bonjour!");
    break;
  case "hi":
    console.log("Shubh din!");
    break;
  default:
    console.log("Error!");
}
